CREATE TABLE IF NOT EXISTS BESTANDSBEWEGUNG (
    BEWEGUNG_ID BIGINT NOT NULL AUTO_INCREMENT,
    RESSOURCE_ID DECIMAL(8,0) NOT NULL,
    BEWEGUNGSART VARCHAR(20) NOT NULL,
    MENGE DECIMAL(12,2) NOT NULL,
    DATUM DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (BEWEGUNG_ID),
    KEY FK_BESTANDSBEWEGUNG_RESSOURCE (RESSOURCE_ID),
    CONSTRAINT FK_BESTANDSBEWEGUNG_RESSOURCE
        FOREIGN KEY (RESSOURCE_ID)
        REFERENCES RESSOURCE (RESSOURCE_ID),
    CONSTRAINT CHK_BESTANDSBEWEGUNG_ART
        CHECK (BEWEGUNGSART IN ('VERBRAUCH', 'NACHSCHUB', 'KORREKTUR'))
);

UPDATE RESSOURCE
SET MENGE_EINHEIT = 'm³',
    MIN_SCHWELLENWERT = 400,
    VERBRAUCH_PRO_SOL = 20
WHERE RESSOURCE_ID = 1;

UPDATE RESSOURCE
SET MENGE_EINHEIT = 'L',
    MIN_SCHWELLENWERT = 1000,
    VERBRAUCH_PRO_SOL = 40
WHERE RESSOURCE_ID = 2;

UPDATE RESSOURCE
SET MENGE_EINHEIT = 'KG',
    MIN_SCHWELLENWERT = 250,
    VERBRAUCH_PRO_SOL = 15
WHERE RESSOURCE_ID = 3;

-- Erzeugt nur dann reproduzierbare Demo-Verbrauchsdaten, wenn noch keine
-- Bestandsbewegungen existieren.
INSERT INTO BESTANDSBEWEGUNG (
    RESSOURCE_ID,
    BEWEGUNGSART,
    MENGE,
    DATUM
)
WITH RECURSIVE tage AS (
    SELECT 0 AS nummer
    UNION ALL
    SELECT nummer + 1
    FROM tage
    WHERE nummer < 364
),
verbrauchswerte AS (
    SELECT 1 AS ressource_id, 20.0 AS basisverbrauch
    UNION ALL
    SELECT 2, 40.0
    UNION ALL
    SELECT 3, 15.0
)
SELECT
    v.ressource_id,
    'VERBRAUCH',
    ROUND(
        v.basisverbrauch
        * (0.90 + MOD(t.nummer * 7 + v.ressource_id * 3, 21) / 100),
        2
    ),
    DATE_SUB(CURRENT_DATE, INTERVAL t.nummer DAY)
FROM tage t
CROSS JOIN verbrauchswerte v
WHERE NOT EXISTS (
    SELECT 1
    FROM BESTANDSBEWEGUNG
);
